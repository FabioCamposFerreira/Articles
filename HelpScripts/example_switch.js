// Switch
let age= 11;
switch (age){
    case 10:
        console.log("age is equal to 10!");
        break;
    case 9:
        console.log("age is equal to 9!");
        break;
    case 8:
        console.log("age is equal to 8!");
        break;
    default:
        console.log("I don't know what is the value of age!");
        break;
}